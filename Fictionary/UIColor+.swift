import UIKit

extension UIColor {
    
    static func pastelGreen() -> UIColor {
        return UIColor(red:0.48, green:0.92, blue:0.51, alpha:1.00)
    }
    
    static func shamrock() -> UIColor {
        return UIColor(red:0.24, green:0.83, blue:0.61, alpha:1.00)
    }
    
    static func medAquamarine() -> UIColor {
        return UIColor(red:0.35, green:0.89, blue:0.69, alpha:1.00)
    }
    
    static func tuatara() -> UIColor {
        return UIColor(red:0.26, green:0.26, blue:0.26, alpha:1.00)
    }
}
